  State<ResultScreen> createState() => _ResultScreenState();
}

class _ResultScreenState extends State<ResultScreen>
    with SingleTickerProviderStateMixin {
  late AnimationController _animationController;
  late Animation<double> _fadeAnimation;
  late Animation<double> _scaleAnimation;

  late String stage;
  late double confidence;
  late String description;
  bool _isSaving = false;

  @override
  void initState() {
    super.initState();

    stage = widget.prediction;
    confidence = widget.confidence;
    description = _getDescription(stage);

    _animationController = AnimationController(
      duration: const Duration(milliseconds: 1000),
      vsync: this,
    );

    _fadeAnimation = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(
        parent: _animationController,
        curve: const Interval(0.0, 0.6, curve: Curves.easeOut),
      ),
    );

    _scaleAnimation = Tween<double>(begin: 0.8, end: 1.0).animate(
      CurvedAnimation(
        parent: _animationController,
        curve: const Interval(0.3, 1.0, curve: Curves.elasticOut),
      ),
    );

    _animationController.forward();
  }

  String _getDescription(String stage) {
    switch (stage.toLowerCase()) {
      case 'non demented':
      case 'nondemented':
      case 'no impairment':
        return "The analysis shows no signs of dementia. Continue regular health check-ups.";
      case 'mild demented':
      case 'milddemented':
      case 'mild impairment':
        return "Early signs of cognitive impairment detected. Consult a healthcare professional for proper diagnosis.";
      case 'moderate demented':
      case 'moderatedemented':
      case 'moderate impairment':
        return "Moderate cognitive impairment detected. Medical consultation is recommended.";
      case 'very mild demented':
      case 'verymilddemented':
      case 'very mild impairment':
        return "Very mild signs of cognitive impairment. Consult with a healthcare professional for evaluation.";
      default:
        return "Analysis complete. Please consult with a healthcare professional for proper interpretation.";
    }
  }

  @override
  void dispose() {
    _animationController.dispose();
    super.dispose();
  }

  Color _getStageColor() {
    final stageLower = stage.toLowerCase();
    if (stageLower.contains("non") || stageLower.contains("no impairment")) {
      return AppColors.success;
    } else if (stageLower.contains("very mild")) {
      return const Color(0xFF10B981);
    } else if (stageLower.contains("mild")) {
      return AppColors.warning;
    } else if (stageLower.contains("moderate")) {
      return const Color(0xFFFF6B35);
    } else {
      return AppColors.error;
    }
  }

  IconData _getStageIcon() {
    final stageLower = stage.toLowerCase();
    if (stageLower.contains("non") || stageLower.contains("no impairment")) {
      return Icons.check_circle_rounded;
    } else if (stageLower.contains("mild")) {
      return Icons.warning_rounded;
    } else {
      return Icons.error_rounded;
    }
  }

