  Future<void> _savePdf() async {
    setState(() => _isSaving = true);
    try {
      final bytes = await _generatePdf();
      final filename =
          'alzheimer_report_${DateFormat('yyyyMMdd_HHmmss').format(DateTime.now())}.pdf';

      Directory? targetDir;

      if (Platform.isAndroid) {
        try {
          final dirs = await getExternalStorageDirectories(
            type: StorageDirectory.downloads,
          );
          if (dirs != null && dirs.isNotEmpty) {
            targetDir = dirs.first;
          } else {
            targetDir = await getExternalStorageDirectory();
          }
        } catch (_) {
          targetDir = await getExternalStorageDirectory();
        }
      } else if (Platform.isIOS) {
        targetDir = await getApplicationDocumentsDirectory();
      } else {
        try {
          targetDir = await getDownloadsDirectory();
        } catch (_) {
          targetDir = await getApplicationDocumentsDirectory();
        }
      }

      if (targetDir != null) {
        final savedPath = '${targetDir.path}/$filename';
        final file = File(savedPath);
        await file.writeAsBytes(bytes);

        if (!mounted) return;
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Report saved to $savedPath'),
            action: SnackBarAction(
              label: 'Open',
              onPressed: () => OpenFile.open(savedPath),
            ),
          ),
        );
      } else {
        await Printing.sharePdf(bytes: bytes, filename: filename);
      }
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text('Failed to save report: $e')));
    } finally {
      if (mounted) setState(() => _isSaving = false);
    }
  }

