  // âœ… NEW: Share PDF function
  Future<void> _sharePdf() async {
    setState(() => _isSaving = true);
    try {
      final bytes = await _generatePdf();
      final filename =
          'alzheimer_report_${DateFormat('yyyyMMdd_HHmmss').format(DateTime.now())}.pdf';

      // Save to temporary directory
      final tempDir = await getTemporaryDirectory();
      final tempFile = File('${tempDir.path}/$filename');
      await tempFile.writeAsBytes(bytes);

      // Share the PDF
      await Share.shareXFiles(
        [XFile(tempFile.path)],
        subject: 'Alzheimer AI Analysis Report',
        text: 'Here is my Alzheimer AI analysis report.',
      );
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text('Failed to share PDF: $e')));
    } finally {
      if (mounted) setState(() => _isSaving = false);
    }
  }

